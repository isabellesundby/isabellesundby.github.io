function drawStump() {
  brush.fillStyle = "#614f42 ";
  for (let i = 0; i < stumps.length; ++i) {
    let stump = stumps[i];
    brush.fillRect(
      stump[0],
      stump[1],
      stump[2],
      stump[3]
    );
  }
}

function processCollision() {
  brush.fillStyle = "#8B4513";
  for (let i = 0; i < stumps.length; ++i) {
    let stump = stumps[i];
    if (
      isXyInRect(frogX + 15, frogY+272, stump[0], stump[1], stump[2], stump[3]) ||
      isXyInRect(frogX + 48, frogY+272, stump[0], stump[1], stump[2], stump[3]) ||
      isXyInRect(
        frogX+15,
        frogY + 302,
         stump[0], stump[1], stump[2], stump[3]
      ) ||
      isXyInRect(
        frogX + 48,
        frogY + 302,
        stump[0], stump[1], stump[2], stump[3]
      )
    ) {
      gameOver();
      break;
    }
  }
}

function drawFrame() {
  updateData();
  drawbackground();
  drawStump();
  drawfrog();
  processCollision();
}

function updateData() {
  frogDy += g; // speed increase by the gravity
  frogY += frogDy; //y position increasement by frogDy
  if(frogY+302 >= 3/4*h){
    frogY = 3/4*h-302;
    frogDy = 0;
  }
  // update the stumps
  for (let i = 0; i < stumps.length; ++i) {
    let stump = stumps[i]; // [100, 50, 50, 100]
    stump[0] -= stumpDx;
    if (stump[0] + stump[2] <= 0) {
      stump[0] = w + Math.floor(Math.random() * 100 + 50);
      ++score;
      document.getElementById("h").innerHTML = "Score: " + score;
    }
  }
}

function resetData() {
  frogY = 200;
  frogDy = 0;
  score = 0;
  stumps = [[200, 280, 20, 20], [300, 260, 20, 40], [400, 290, 20, 10]];
}

function onkeydown(e) {
  if (e.key === "Enter") {
    resetData();
    clearInterval(timerId);
    timerId = setInterval(drawFrame, 20);
  } else if (e.key === " ") {
    frogDy -= jumpImpact;
  }
}